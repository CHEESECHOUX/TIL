#DFS 예제
#DFS 메서드 정의
def dfs(graph, v, visited):
    #현재 노드를 방문 처리
    visited[v] = True
    #방문했으면 v에 번호 출력
    print(v, end=' ')
    #현재 확인하고 있는 노드(최상단 원소)와 연결된 다른 노드를 재귀적으로 방문
    for i in graph[v]:
        #인접한 노드가 방문하지 않은 상태라면
        if not visited[i]:
            #재귀함수를 통해 방문처리
            dfs(graph, i, visited)

#각 노드가 연결된 정보를 리스트 자료형으로 표현(2차원 리스트)
graph = [
    [],        #그래프 노드의 번호가 1번부터라 index 0은 비워두기
    [2, 3, 8], #1번 노드와 연결된 노드들
    [1, 7],    #2번 노드와 연결된 노드들
    [1, 4, 5], #3번 노드와 연결된 노드들
    [3, 5],    #4번 노드와 연결된 노드들
    [3, 4],    #5번 노드와 연결된 노드들
    [7],       #6번 노드와 연결된 노드들
    [2, 6, 8], #7번 노드와 연결된 노드들
    [1, 7]     #8번 노드와 연결된 노드들
]

#각 노드가 방문된 정보를 리스트 자료형으로 표현(1차원 리스트) 
#False로 방문하지 않은 것으로 1차원 리스트를 초기화
visited = [False] * 9

#정의된 DFS 함수 호출
dfs(graph, 1, visited)
# 1 2 7 6 8 3 4 5

