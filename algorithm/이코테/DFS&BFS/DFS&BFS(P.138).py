#DFS : 한 우물을 깊게 파면서 탐색
#깊이 우선 탐색 (최대한 멀리 있는 노드를 우선으로 탐색) 
#동작원리 : 스택
#구현방법 : 재귀함수 이용

#BFS : 여러 우물을 동시에 같은 깊이로 탐색
#너비 우선 탐색 (가까운 노드부터 탐색)
#선입선출 방식인 큐 자료구조를 이용. dfs보다 속도가 조금 더 빠르다.
#동작원리 : 큐
#구현방법 : 큐 자료구조 이용

